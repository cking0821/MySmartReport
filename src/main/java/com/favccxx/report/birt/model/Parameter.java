package com.favccxx.report.birt.model;

public class Parameter {
	
	private String name;
	
	private String paramName;
	
	private String nativeName;
	
	private String dataType;
	
	private String nativeDataType;
	
	private String position;
	
	private String defaultValue;
	
	private String isInput;
	
	private String isOutput;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getParamName() {
		return paramName;
	}

	public void setParamName(String paramName) {
		this.paramName = paramName;
	}

	public String getNativeName() {
		return nativeName;
	}

	public void setNativeName(String nativeName) {
		this.nativeName = nativeName;
	}

	public String getDataType() {
		return dataType;
	}

	public void setDataType(String dataType) {
		this.dataType = dataType;
	}

	public String getNativeDataType() {
		return nativeDataType;
	}

	public void setNativeDataType(String nativeDataType) {
		this.nativeDataType = nativeDataType;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getDefaultValue() {
		return defaultValue;
	}

	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}

	public String getIsInput() {
		return isInput;
	}

	public void setIsInput(String isInput) {
		this.isInput = isInput;
	}

	public String getIsOutput() {
		return isOutput;
	}

	public void setIsOutput(String isOutput) {
		this.isOutput = isOutput;
	}
	
	

}
